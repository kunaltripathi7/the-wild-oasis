export async function getBookings({ filter, sortBy, page }) {
  // const { data, error } = await supabase.from("bookings").select(
  //   // limit data loading to fix storage issue
  //   "id, created_at, startDate, endDate, numNights, numGuests, status, totalPrice, cabins(name), guests(fullName, email)"
  // );
  // // .eq("status", "unconfirmed"); gte
  // // server side processing instead of client side in cabins table better as downloaded data is less

  let query = supabase.from("bookings").select(
    "id, created_at, startDate, endDate, numNights, numGuests, status, totalPrice, cabins(name), guests(fullName, email)",
    { count: "exact" } // returns the count of results
  );

  if (filter) query = query[filter.method || "eq"](filter.field, filter.value);
  if (sortBy)
    query = query.order(sortBy.field, {
      ascending: sortBy.direction === "asc",
    });

  if (page) {
    const from = (page - 1) * PAGE_SIZE;
    const to = PAGE_SIZE * page - 1;
    query = query.range(from, to);
  }

  const { data, error, count } = await query;

  if (error) {
    console.log(error);
    throw new Error("Bookings can't be loaded");
  }
  return { data, count };
} // can't get useSearchParams hook over here cuz hooks r needed to be used inside a compo
